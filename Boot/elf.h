#ifndef __ELF_H__
#define __ELF_H__

#include <stdint.h>
#include "xnldr.h"

/* ELF Magic */
#define ELF_MAGIC 0x464C457F

/* File Types */
#define ET_NONE 0
#define ET_REL  1
#define ET_EXEC 2
#define ET_DYN  3
#define ET_CORE 4

/* ELF64 Header */
typedef struct {
    uint8_t e_ident[16];
    uint16_t e_type;
    uint16_t e_machine;
    uint32_t e_version;
    uint64_t e_entry;
    uint64_t e_phoff;
    uint64_t e_shoff;
    uint32_t e_flags;
    uint16_t e_ehsize;
    uint16_t e_phentsize;
    uint16_t e_phnum;
    uint16_t e_shentsize;
    uint16_t e_shnum;
    uint16_t e_shstrndx;
} Elf64_Ehdr;

/* Program Header Types */
#define PT_NULL    0
#define PT_LOAD    1
#define PT_DYNAMIC 2
#define PT_INTERP  3
#define PT_NOTE    4
#define PT_SHLIB   5
#define PT_PHDR    6
#define PT_TLS     7

/* Program Header Flags */
#define PF_X 0x1
#define PF_W 0x2
#define PF_R 0x4

/* ELF64 Program Header */
typedef struct {
    uint32_t p_type;
    uint32_t p_flags;
    uint64_t p_offset;
    uint64_t p_vaddr;
    uint64_t p_paddr;
    uint64_t p_filesz;
    uint64_t p_memsz;
    uint64_t p_align;
} Elf64_Phdr;

/*
 * XELFLoadImage -- loads an ELF64 executable image to memory
 * @param filebuf -- file buffer
 */
extern void* XELFLoadImage(void* filebuf);

/*
 * XELFGetEntryPoint -- returns the entry point 
 * of loaded ELF image
 * @param image -- Pointer to file buffer
 */
extern XEImageEntry XELFGetEntryPoint(void* image);

#endif // __ELF_H__
